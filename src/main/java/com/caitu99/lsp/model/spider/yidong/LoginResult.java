public class LoginResult {
    private int errcode;
    private String artifact;
    private String assertAcceptURL;
    private String code;
    private String desc;
    private String isloal;
    private String result;
    private String uid;
    private String succcb;

    public int getErrcode() {
        return errcode;
    }

    public String getArtifact() {
        return artifact;
    }

    public void setArtifact(String artifact) {
        this.artifact = artifact;
    }

    public String getAssertAcceptURL() {
        return assertAcceptURL;
    }

    public void setAssertAcceptURL(String assertAcceptURL) {
        this.assertAcceptURL = assertAcceptURL;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getIsloal() {
        return isloal;
    }

    public void setIsloal(String isloal) {
        this.isloal = isloal;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getSucccb() {
        return succcb;
    }

    public void setSucccb(String succcb) {
        this.succcb = succcb;
    }


}
